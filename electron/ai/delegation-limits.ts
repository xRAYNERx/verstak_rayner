/**
 * Лимиты динамического дерева делегирования (Фаза 4, Идея 3).
 *
 * До Фазы 4 суб-агентам было ЗАПРЕЩЕНО делегировать вообще (delegate_* лежали
 * в SUBAGENT_FORBIDDEN_TOOLS) — грубая, но надёжная защита от рекурсии. Фаза 4
 * разрешает субам делегировать дальше, но под ДВУМЯ строгими лимитами:
 *
 *  1) ГЛУБИНА (depth). Главный агент = depth 0, его субы = depth 1, их субы =
 *     depth 2 и т.д. Делегировать может только агент с depth < MAX_DELEGATION_DEPTH.
 *     На предельной глубине delegate_* субу недоступен (вырезается из whitelist).
 *
 *  2) ОБЩЕЕ КОЛИЧЕСТВО агентов за прогон (MAX_TOTAL_AGENTS_PER_SESSION). Даже
 *     при дозволенной глубине дерево могло бы взорваться вширь (каждый суб
 *     порождает по 10 субов). Счётчик SessionAgentCounter считает ВСЕХ субов
 *     дерева за один ai:send; при достижении потолка новые делегирования
 *     блокируются с понятной ошибкой.
 *
 * Эти лимиты — НЕ опциональны. Они единственная преграда между «контролируемое
 * дерево» и «бесконечная рекурсия + взрыв стоимости». Покрыты юнит-тестами
 * (tests/ai/delegation-limits.test.ts).
 *
 * Модуль чистый (без Electron/БД) — тестируется в vitest.
 */

/**
 * Максимальная глубина делегирования. depth < MAX_DELEGATION_DEPTH может
 * делегировать дальше; на depth === MAX_DELEGATION_DEPTH — уже нет.
 *
 * 2 означает: главный (0) → суб (1) → под-суб (2). Под-суб (depth 2) делегировать
 * НЕ может. Три уровня дерева — достаточно для реальной декомпозиции, но дерево
 * остаётся обозримым и дешёвым.
 */
export const MAX_DELEGATION_DEPTH = 2

/**
 * Потолок общего числа суб-агентов за один прогон главного агента (ai:send).
 * Считаются ВСЕ узлы дерева на всех глубинах. 100 — щедро для легитимной работы
 * (рой + вложенные делегирования), но режет экспоненциальный взрыв.
 */
export const MAX_TOTAL_AGENTS_PER_SESSION = 100

/** Результат гейта делегирования: разрешено или причина отказа. */
export interface DelegationGate {
  allowed: boolean
  /** Человекочитаемая причина отказа (для tool_result субу). */
  reason?: string
}

/**
 * Чистый гейт: может ли агент на глубине `depth` запустить ещё `count` субов
 * при уже потраченных `total` агентах сессии. Проверяет ОБА лимита.
 *
 * - depth >= MAX_DELEGATION_DEPTH → нельзя (достигнут предел глубины).
 * - total + count > MAX_TOTAL_AGENTS_PER_SESSION → нельзя (предел количества).
 */
export function checkDelegationAllowed(depth: number, total: number, count: number = 1): DelegationGate {
  if (depth >= MAX_DELEGATION_DEPTH) {
    return {
      allowed: false,
      reason: `Достигнут лимит глубины делегирования (depth=${depth}, максимум ${MAX_DELEGATION_DEPTH}). Суб-агент на этой глубине не может делегировать дальше — выполни задачу сам или верни результат.`
    }
  }
  if (total + count > MAX_TOTAL_AGENTS_PER_SESSION) {
    return {
      allowed: false,
      reason: `Достигнут лимит общего числа агентов за прогон (${total}/${MAX_TOTAL_AGENTS_PER_SESSION}). Новые делегирования заблокированы — заверши работу имеющимися результатами.`
    }
  }
  return { allowed: true }
}

/**
 * Счётчик агентов одного прогона (ai:send). Один инстанс на главный agent-loop,
 * прокидывается через ToolContext во ВСЕ вложенные субы → общий потолок на всё
 * дерево, а не на отдельную ветку. Потокобезопасность не нужна (Node однопоточен,
 * а инкремент синхронный).
 */
export class SessionAgentCounter {
  private total = 0

  /** Сколько агентов уже создано за прогон (всех глубин). */
  get count(): number {
    return this.total
  }

  /**
   * Попытаться зарезервировать `n` агентов для запуска с глубины `depth`.
   * Проверяет оба лимита атомарно: если хватает квоты И глубина в пределах —
   * увеличивает счётчик и возвращает allowed. Иначе НЕ трогает счётчик.
   */
  tryReserve(depth: number, n: number = 1): DelegationGate {
    const gate = checkDelegationAllowed(depth, this.total, n)
    if (gate.allowed) this.total += n
    return gate
  }

  /**
   * Вернуть `n` ранее зарезервированных слотов — суб не стартовал (нет
   * провайдера/apiKey, отменён в очереди, batchCapped). Без рефанда упавшие на
   * валидации субы завышали счётчик дерева и могли преждевременно упереться в
   * MAX_TOTAL_AGENTS_PER_SESSION. Не уходит ниже 0.
   */
  release(n: number = 1): void {
    this.total = Math.max(0, this.total - n)
  }
}
